export const getBeverageById = (id: string) => beverages.find(b => b.id === id);
export const getFeaturedBeverage = () => beverages[0];
export const getPremiumBeverages = () => beverages.filter(b => b.isPremium);
export const getFreeBeverages = () => beverages.filter(b => !b.isPremium);
export const getBeveragesByCategory = (cat: string) => beverages.filter(b => b.category === cat);
export const searchBeverages = (query: string) => {
  const q = query.toLowerCase();
  return beverages.filter(b =>
    b.name.toLowerCase().includes(q) ||
    b.description.toLowerCase().includes(q) ||
    b.tags.some(t => t.includes(q)) ||
    b.category.includes(q)
  );
};
