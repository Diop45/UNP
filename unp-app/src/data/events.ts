export const getEventById = (id: string) => events.find(e => e.id === id);
export const getFeaturedEvent = () => events[0];
export const getEventsByTimeSlot = (slot: string) => events.filter(e => e.timeSlot === slot);
export const searchEvents = (query: string) => {
  const q = query.toLowerCase();
  return events.filter(e =>
    e.title.toLowerCase().includes(q) ||
    e.venue.name.toLowerCase().includes(q) ||
    e.tags.some(t => t.includes(q))
  );
};
